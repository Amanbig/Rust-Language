fn main() {
    let a: i32 = 10; // integer type
    let b: f64 = 20.5; // floating point type
    let c: char = 'A'; // character type
    let d: bool = true; // boolean type
    let e: &str = "Hello, Rust!"; // string slice type
    println!("Integer: {}, Float: {}, Char: {}, Bool: {}, String: {}", a, b, c, d, e);
}
